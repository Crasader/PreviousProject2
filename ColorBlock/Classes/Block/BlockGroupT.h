#pragma once
#include "BlockGroup.h"

class BlockGroupT : public BlockGroup
{
public:
	BlockGroupT(void);
	virtual ~BlockGroupT(void);

	//公共接口
public:
	/************************************************************************/
	/* 初始化图形                                                           */
	/************************************************************************/
	virtual bool Init();

	/************************************************************************/
	/* 旋转图形(顺时针)                                                     */
	/************************************************************************/
	virtual bool Rotate(const vector<BlockObject>& blocks);

	/************************************************************************/
	/* 加速下落图形                                                         */
	/************************************************************************/
	virtual void SpeedUp();

protected:
	/************************************************************************/
	/* 设置图形类型                                                         */
	/************************************************************************/
	virtual void SetBlockGroupType();
};

