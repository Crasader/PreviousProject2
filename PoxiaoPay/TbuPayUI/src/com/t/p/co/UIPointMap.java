package com.t.p.co;

public class UIPointMap {
	private int uiPoint;
	private String type;
	private int ppoint;

	public int getUiPoint() {
		return uiPoint;
	}

	public void setUiPoint(int uiPoint) {
		this.uiPoint = uiPoint;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public int getPpoint() {
		return ppoint;
	}

	public void setPpoint(int ppoint) {
		this.ppoint = ppoint;
	}

}
